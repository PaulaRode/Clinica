    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        //Verificar se tem uma linha da grade selecionada
        if (tblCliente.getSelectedRow() != -1) {
            //Se tiver pegar o código do usuário da grade
            int linhaSelecionada = tblCliente.getSelectedRow();
            String textoCelula = tblCliente.getValueAt(linhaSelecionada, 0).toString();

            //converter o texto da célula em inteiro
            int id = Integer.parseInt(textoCelula);

            //com o id eu vou chamar um método de deletar no controller
            ClienteController controller = new ClienteController();
            if (controller.deletar(id)) {
                pesquisar();
                JOptionPane.showMessageDialog(rootPane, "Cliente deletado com sucesso");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível excluir");
            }
        }
    }//GEN-LAST:event_btnExcluirMouseClicked

