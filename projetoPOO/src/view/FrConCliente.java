    public void pesquisar() {
        //Pega o modelo da grade com suas colunas
        DefaultTableModel modeloTabela = (DefaultTableModel) tblCliente.getModel();
        //Limpa a grade setando o número de linhas para zero
        modeloTabela.setNumRows(0);
        //Cria um ClienteController para poder acessar os dados de tbcliente
        ClienteController controller = new ClienteController();
        List<Cliente> listaCliente = controller.consultar();

        for (Cliente cliente : listaCliente) {
            //cria um array onde cada posição é o valor das colunas da grade
            Object[] linha = {
                cliente.getId(),
                cliente.getNome(),
                cliente.getEmailCli(),
                cliente.getCpf(),
                cliente.getDataNasc(),
                Utils.converterDateToString(cliente.getDataNasc()),};
            modeloTabela.addRow(linha);
        }
    }

