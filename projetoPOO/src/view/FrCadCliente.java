    public void gravar() {
        // Criação de um objeto Cliente com os dados da interface
        Cliente cliente = new Cliente();
        cliente.setNome(txtNome.getText());
        cliente.setEmailCli(txtEmail.getText());
        cliente.setCpf(txtCPF.getText());

        // primeiro declara
        Date dataNasc = Utils.converterStringToDate(txtDataNasc.getText());
        cliente.setDataNasc(dataNasc);

        //depois passo o objeto para o controller e ele irá gravar no banco de dados
        ClienteController controller = new ClienteController();
        if (controller.inserirCliente(cliente)) {
            JOptionPane.showMessageDialog(null,
                    "Cliente cadastrado com sucesso");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null,
                    "Erro ao cadastrar");
        }
    }//GEN-LAST:event_btnSalvarMouseClicked

