    public boolean verificarCampos() {
        if (!txtNome.getText().matches("^[\\p{L} ]+$")) {//a-
            JOptionPane.showMessageDialog(null,
                    "O campo 'Nome' possui formato inválido");
            return false;
        }

        if (!txtEmail.getText().matches("^[a-z0-9._-]+@[a-z0-9._-]+.[a-z._]+$")) {
            JOptionPane.showMessageDialog(null,
                    "O campo 'Email' possui formato inválido");
            return false;
        }

        if (txtDataNasc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "O campo 'Data de Nascimento' está em branco");
            return false;
        }

        return true;
    }

